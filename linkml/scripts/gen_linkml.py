def build_common_schema() -> dict:
    """Schema for shared cross-module definitions."""
    schema = schema_header(
        "common",
        (
            "Cross-cutting definitions shared by other OPA submodules (currently the "
            "``Any`` placeholder used for heterogeneous source values). Kept in a "
            "separate module so that re-imports from the umbrella schema do not "
            "produce duplicate-class merge conflicts."
        ),
    )
    schema["classes"] = {
        "Any": {
            "description": (
                "Free-form value placeholder (scalar, list, or object). Used to model "
                "source slots whose representation is heterogeneous or intentionally "
                "open (e.g. ``TypeDecl.key``, ``AuthorizationInput.body``)."
            ),
            "class_uri": "linkml:Any",
            "comments": [
                "Single shared definition mapped to ``linkml:Any`` (per metamodel) to avoid duplicate-class collisions across imported modules.",
                "Usage is concentrated at real polymorphism boundaries: free-form HTTP payloads, bundle metadata/annotations, discriminated-union payloads, and upstream-opaque JSON Schema slots.",
                "Usage is localized (roughly 11 sites across ~60 classes); most slots remain explicitly typed rather than defaulting to ``Any``.",
            ],
        },
    }
    return schema


