def _normalize_typedecl(node: Any) -> Any:
    """Normalize a TypeDecl-shaped node: ``of`` is always a list of TypeDecls.

    The source capabilities.json uses ``of`` as either a single TypeDecl (for
    ``set``) or a list of TypeDecls (for ``any``). LinkML multivalued slots
    require list-form, so we wrap scalar ``of`` values as one-element lists.
    Recurses into ``of``, ``dynamic``, ``static``, ``key``, ``value``.
    """
    if not isinstance(node, dict):
        return node
    out: dict[str, Any] = {}
    for k, v in node.items():
        if k == "of":
            if isinstance(v, list):
                out[k] = [_normalize_typedecl(item) for item in v]
            else:
                out[k] = [_normalize_typedecl(v)]
        elif k in ("dynamic", "key", "value"):
            out[k] = _normalize_typedecl(v)
        elif k == "static":
            out[k] = [_normalize_typedecl(item) for item in v]
        elif k == "args":
            out[k] = [_normalize_typedecl(item) for item in v]
        elif k == "result":
            out[k] = _normalize_typedecl(v)
        elif k == "variadic":
            out[k] = _normalize_typedecl(v)
        else:
            out[k] = v
    return out


def build_capabilities_instance(data: dict) -> dict:
    """Return capabilities.json reshaped as a Capabilities instance."""
    instance: dict[str, Any] = {}
    if data.get("builtins"):
        instance["builtins"] = [
            {**b, "decl": _normalize_typedecl(b["decl"])} if "decl" in b else b
            for b in data["builtins"]
        ]
    if data.get("future_keywords"):
        instance["future_keywords"] = list(data["future_keywords"])
    if data.get("wasm_abi_versions"):
        instance["wasm_abi_versions"] = data["wasm_abi_versions"]
    if data.get("features"):
        instance["features"] = list(data["features"])
    if data.get("allow_net") is not None:
        instance["allow_net"] = list(data["allow_net"])
    return instance


