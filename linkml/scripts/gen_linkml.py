    """Reshape ``builtin_metadata.json`` into a BuiltinMetadataCatalog instance.

    The source mixes ``_categories`` with flat builtin entries at the top level;
    here we lift them under explicit ``categories`` and ``builtins`` keys keyed
    by name so the document matches the LinkML schema exactly.
    """
    cats = data.get("_categories") or {}
    categories = {
        _slug(name): {
            "name": _slug(name),
            "category": _slug(name),
            "builtins": list(members),
        }
        for name, members in sorted(cats.items())
    }

    builtins: dict[str, Any] = {}
    for name, entry in sorted(data.items()):
        if name.startswith("_"):
            continue
        if not isinstance(entry, dict):
            continue
        record: dict[str, Any] = {"name": name}
        for key in (
            "description",
            "introduced",
            "available",
            "wasm",
            "infix",
            "deprecated",
            "relation",
        ):
            if key in entry and entry[key] not in (None, ""):
                record[key] = entry[key]
        if entry.get("args"):
            record["args"] = list(entry["args"])
        if entry.get("result"):
            record["result"] = entry["result"]
        builtins[name] = record

    return {"categories": categories, "builtins": builtins}


