def _source_url(rel_path: str) -> str:
    """Return the upstream URL for an OPA source artifact (used for ``source:``)."""
    return _OPA_SOURCE_BASE + rel_path


HEADER = (
    "# AUTOGENERATED by linkml/scripts/gen_linkml.py from upstream OPA sources.\n"
    "# Do not edit by hand; rerun `just gen-linkml`.\n"
)


# --------------------------------------------------------------------------- #
# YAML dumping helpers (deterministic, key-ordered, literal block strings)    #
# --------------------------------------------------------------------------- #


class _Dumper(yaml.SafeDumper):
    def increase_indent(self, flow=False, indentless=False):  # type: ignore[override]
        return super().increase_indent(flow, False)


def _represent_mapping(dumper: yaml.Dumper, data: dict) -> Any:
    return dumper.represent_mapping("tag:yaml.org,2002:map", data.items())


def _represent_str(dumper: yaml.Dumper, data: str) -> Any:
    if "\n" in data:
        return dumper.represent_scalar("tag:yaml.org,2002:str", data, style="|")
    return dumper.represent_scalar("tag:yaml.org,2002:str", data)


_Dumper.add_representer(dict, _represent_mapping)
_Dumper.add_representer(OrderedDict, _represent_mapping)
_Dumper.add_representer(str, _represent_str)


def dump_yaml(path: Path, data: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    # width=78 keeps generator output within the yamllint warn threshold of 88
    # columns (the config allows long unbreakable URLs / CURIEs to spill).
    text = yaml.dump(
        data,
        Dumper=_Dumper,
        sort_keys=False,
        default_flow_style=False,
        width=78,
        allow_unicode=True,
    )
    path.write_text(HEADER + _blank_line_entities(_blank_line_sections(text)))
    rel = path.relative_to(LINKML_ROOT)
    print(f"wrote {rel}", file=sys.stderr)


# Top-level schema sections that should be visually separated by blank lines
# in the rendered YAML (SKILL guidance: "Separate major/minor elements with
# blank lines for readability (i.e. prefixes, imports, classes, slots, enums,
# subsets and sub-elements)").
_TOP_LEVEL_SECTIONS = (
    "prefixes:",
    "imports:",
    "source:",
    "see_also:",
    "annotations:",
    "types:",
    "subsets:",
    "slots:",
    "classes:",
    "enums:",
)


def _blank_line_sections(text: str) -> str:
    """Insert a blank line before each top-level schema section header."""
    out_lines: list[str] = []
    for line in text.splitlines():
        if (
            out_lines
            and out_lines[-1] != ""
            and any(line.startswith(s) for s in _TOP_LEVEL_SECTIONS)
        ):
            out_lines.append("")
        out_lines.append(line)
    return "\n".join(out_lines) + "\n"


def _blank_line_entities(text: str) -> str:
    """Insert blank lines between notable entities for readability.

    Adds spacing between sibling entries in top-level mapping-heavy sections:
    ``subsets``, ``types``, ``slots``, ``classes``, and ``enums``.
    """
    spaced: list[str] = []
    current_section: str | None = None
    entity_sections = {"subsets", "types", "slots", "classes", "enums"}
    top_entity_key = re.compile(r"^  [^\s].*:$")

    for line in text.splitlines():
        if re.match(r"^[a-z_]+:$", line):
            current_section = line[:-1]

        if (
            current_section in entity_sections
            and top_entity_key.match(line)
            and spaced
            and spaced[-1] != ""
            and top_entity_key.match(spaced[-1])
        ):
            spaced.append("")

        spaced.append(line)

    return "\n".join(spaced) + "\n"


# --------------------------------------------------------------------------- #
# Common schema-header builder                                                #
# --------------------------------------------------------------------------- #


def schema_header(local: str, description: str) -> dict:
    """Return a LinkML schema header for module ``opa.<local>``.

    Prefix choice notes (per SKILL ``Prefixes Reference``):

    * ``openpolicyagent`` is used instead of the natural ``opa`` because the
      latter is already canonicalised by bioregistry to an unrelated namespace
      (``adream``), which would trip ``linkml-lint``'s ``canonical_prefixes``
      rule. The IRI still matches the schema ``id``.
    * ``rdfs``, ``dcterms`` and ``schema`` are declared up-front because the
      common slot-URI post-processor (``_align_common_slot_uris``) maps
      ``name`` / ``description`` / ``title`` slots to those vocabularies.
    """
    core_subset = f"{local}_core_elements"
    supporting_subset = f"{local}_supporting_elements"

    return {
        "id": f"https://w3id.org/lmodel/opa/{local}",
        "name": local,
        "title": local.replace("_", " ").title(),
        "description": description,
        "license": "Apache-2.0",
        "version": SCHEMA_VERSION,
        "prefixes": {
            "openpolicyagent": "https://w3id.org/lmodel/opa/",
            "linkml": "https://w3id.org/linkml/",
            "schema": "http://schema.org/",
            "dcterms": "http://purl.org/dc/terms/",
            "rdfs": "http://www.w3.org/2000/01/rdf-schema#",
        },
        "default_prefix": "openpolicyagent",
        "default_range": "string",
        "imports": ["linkml:types"],
        "subsets": {
            core_subset: {
                "description": "Primary document/root entities for this module.",
            },
            supporting_subset: {
                "description": "Supporting structural or helper entities.",
            },
        },
    }


# --------------------------------------------------------------------------- #
# Cross-cutting schema post-processors (FAIR alignment, hygiene)              #
# --------------------------------------------------------------------------- #


def _align_common_slot_uris(schema: dict) -> None:
    """Map common attribute names to canonical FAIR vocabulary URIs.

    SKILL guidance: when a slot is semantically equal to a term in DC,
    schema.org, FOAF, RDFS, etc., wire its ``slot_uri`` (or ``exact_mappings``
    for identifier slots) to that resolvable term instead of leaving it as the
    schema-local default.
    """
    for cls in schema.get("classes", {}).values():
        for slot_name, slot_def in (cls.get("attributes") or {}).items():
            if not isinstance(slot_def, dict):
                continue
            if slot_name == "name":
                if slot_def.get("identifier"):
                    slot_def.setdefault(
                        "exact_mappings",
                        ["schema:identifier", "dcterms:identifier"],
                    )
                else:
                    slot_def.setdefault("slot_uri", "rdfs:label")
            elif slot_name == "description":
                slot_def.setdefault("slot_uri", "schema:description")
            elif slot_name == "title":
                slot_def.setdefault("slot_uri", "dcterms:title")


def _dedupe_enum_descriptions(schema: dict) -> None:
    """Drop ``description: <symbol>`` entries that add no information.

    Many source-driven enums (e.g. ``BuiltinTypeKind``) carry permissible
    values whose only documentation is the symbol itself. Removing the
    redundant ``description`` keeps the rendered YAML focused on entries that
    actually carry extra context (slugged symbols, ``meaning`` mappings, etc.).
    """
    for enum_def in (schema.get("enums") or {}).values():
        for symbol, pv in (enum_def.get("permissible_values") or {}).items():
            if not isinstance(pv, dict):
                continue
            if pv.get("description") == symbol:
                pv.pop("description", None)


def _finalize_schema(schema: dict) -> dict:
    """Apply all cross-cutting post-processors in a fixed order."""
    _align_common_slot_uris(schema)
    _dedupe_enum_descriptions(schema)
    _add_default_subset_membership(schema)
    return schema


def _add_default_subset_membership(schema: dict) -> None:
    """Assign each class to a default subset for navigation and grouping."""
    subsets = schema.get("subsets") or {}
    if not isinstance(subsets, dict):
        return
    core_subset = next((k for k in subsets if k.endswith("_core_elements")), None)
    supporting_subset = next(
        (k for k in subsets if k.endswith("_supporting_elements")), None
    )
    if not core_subset or not supporting_subset:
        return

    classes = schema.get("classes") or {}
    if not isinstance(classes, dict):
        return
    for class_def in classes.values():
        if not isinstance(class_def, dict):
            continue
        in_subset = class_def.get("in_subset")
        if in_subset:
            continue
        if class_def.get("tree_root"):
            class_def["in_subset"] = [core_subset]
        else:
            class_def["in_subset"] = [supporting_subset]


