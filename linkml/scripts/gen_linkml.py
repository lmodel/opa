# --------------------------------------------------------------------------- #
# Generic JSON-Schema (draft 2020-12) -> LinkML converter                     #
# --------------------------------------------------------------------------- #
#
# Used by the ``bundle_manifest`` and ``ir_plan`` generators below. Both
# upstream files ship as JSON Schema 2020-12 with ``$defs``-based modular
# class definitions, a top-level ``$ref`` naming the root class, and (for IR
# plan) ``oneOf`` discriminator wrappers for sum types.
#
# Limitations -- this is intentionally narrow rather than a fully-general
# JSON-Schema-to-LinkML converter:
#
# * Only ``type: object`` defs translate to LinkML classes; ``oneOf`` defs
#   become tagged-wrapper classes with a ``type`` slot and a payload of
#   range ``Any`` (see ``_convert_oneof_def``).
# * ``additionalProperties`` is informational only; LinkML does not enforce
#   closed-shape semantics at validation time for inline attributes.
# * Nullable types written as ``type: [array, null]`` are flattened to the
#   non-null variant (LinkML uses ``required: false`` rather than nullable).
# * Property names that collide with Python keywords are remapped via
#   ``_PYTHON_KEYWORD_REMAP``; the original wire-format key is preserved in
#   the slot description so consumers can round-trip to upstream JSON.


# Python keywords that appear as JSON Schema property names in OPA's upstream
# schemas and would otherwise produce invalid Python from ``gen-python``.
# The renamed slot still validates LinkML instances; downstream tooling that
# needs to round-trip with raw OPA output must apply this rename (e.g. via a
# JSON key-rename step).
_PYTHON_KEYWORD_REMAP = {
    "return": "return_value",
}


def _jsonschema_to_linkml_range(spec: dict) -> str:
    """Map a JSON-Schema property ``type`` to a LinkML range name."""
    t = spec.get("type")
    if isinstance(t, list):
        # Nullable wrappers: pick the first non-null member.
        t = next((v for v in t if v != "null"), None)
    return {
        "string": "string",
        "integer": "integer",
        "number": "float",
        "boolean": "boolean",
        "object": "Any",
    }.get(t, "string")


def _convert_property(spec: dict, prop: str, owner: str) -> dict:
    """Translate one JSON-Schema property to a LinkML attribute definition.

    Handles:
    * ``$ref`` -> range = referenced class name.
    * ``type: array`` with ``items: {$ref}`` -> multivalued range.
    * ``type: array`` with primitive items -> multivalued primitive range.
    * Scalar ``type`` -> primitive range.

    Always sets a ``description`` (synthesised from ``prop``/``owner`` when
    the source schema does not provide one) so ``linkml-lint`` does not warn
    about the auto-generated slots from ``$defs``.
    """
    attr: dict[str, Any] = {}
    if "description" in spec:
        attr["description"] = spec["description"]
    else:
        attr["description"] = f"``{prop}`` field of ``{owner}`` (from upstream JSON Schema)."
    if "$ref" in spec:
        attr["range"] = spec["$ref"].rsplit("/", 1)[-1]
        attr["inlined"] = True
        return attr
    t = spec.get("type")
    if isinstance(t, list):
        t = next((v for v in t if v != "null"), None)
    if t == "array":
        items = spec.get("items") or {}
        if "$ref" in items:
            attr["range"] = items["$ref"].rsplit("/", 1)[-1]
            attr["inlined"] = True
            attr["inlined_as_list"] = True
        else:
            attr["range"] = _jsonschema_to_linkml_range(items)
        attr["multivalued"] = True
        return attr
    if t == "object":
        # ``additionalProperties`` map -> open object, modelled as Any.
        attr["range"] = "Any"
        attr["inlined"] = True
        return attr
    attr["range"] = _jsonschema_to_linkml_range(spec)
    return attr


def _convert_object_def(name: str, defn: dict) -> dict:
    """Translate a JSON-Schema object def to a LinkML class definition."""
    required = set(defn.get("required") or [])
    attributes: dict[str, dict] = {}
    for prop, spec in (defn.get("properties") or {}).items():
        slot_name = _PYTHON_KEYWORD_REMAP.get(prop, prop)
        attr = _convert_property(spec, prop, name)
        if slot_name != prop:
            attr["description"] = (
                f"{attr['description']} "
                f"(Renamed from upstream wire-format key ``{prop}`` to avoid the "
                "Python reserved word.)"
            )
        if prop in required:
            attr["required"] = True
        attributes[slot_name] = attr
    cls: dict[str, Any] = {
        "description": defn.get("description") or f"``{name}`` definition from upstream JSON Schema.",
    }
    if attributes:
        cls["attributes"] = attributes
    return cls


def _convert_oneof_def(name: str, defn: dict) -> tuple[dict, list[str]]:
    """Translate a ``oneOf`` discriminator def to a tagged-wrapper class.

    The IR plan schema models sum types (``Stmt``, ``Val``) as a ``oneOf`` of
    objects of shape ``{type: const <Tag>, <payload>: $ref <Tag>}``. We
    surface this as a single LinkML class with:

    * ``type`` slot -- a string carrying the discriminator tag (the set of
      allowed values is captured as a sibling enum so consumers can validate
      them; returned alongside the class).
        * Payload slot (``stmt``/``value``/...) -- ``range: Any`` plus ``any_of``
            over all referenced payload classes, so generated JSON Schema can emit
            a concrete ``anyOf`` union while retaining an escape hatch when upstream
            variants evolve.
    """
    tag_to_payload: list[tuple[str, str, str]] = []  # (tag, payload_slot, payload_class)
    payload_ranges: set[str] = set()
    payload_slot_name: str | None = None
    for variant in defn.get("oneOf") or []:
        props = variant.get("properties") or {}
        tag = (props.get("type") or {}).get("const")
        if not tag:
            continue
        for slot, slot_spec in props.items():
            if slot == "type":
                continue
            if "$ref" in slot_spec:
                payload_class = slot_spec["$ref"].rsplit("/", 1)[-1]
                tag_to_payload.append((tag, slot, payload_class))
                payload_ranges.add(payload_class)
                payload_slot_name = payload_slot_name or slot
                break
            payload_range = _jsonschema_to_linkml_range(slot_spec)
            tag_to_payload.append((tag, slot, payload_range))
            payload_ranges.add(payload_range)
            payload_slot_name = payload_slot_name or slot
            break
    payload_slot_name = payload_slot_name or "value"
    tags = [t for t, _, _ in tag_to_payload]
    tag_mapping = "\n".join(
        f"* ``{tag}`` -- payload class ``{cls}``"
        for tag, _, cls in tag_to_payload
    )
    payload_any_of = [{"range": range_name} for range_name in sorted(payload_ranges)]

    cls = {
        "description": (
            f"Tagged-wrapper class for the ``{name}`` discriminated union from\n"
            "the upstream JSON Schema.\n\n"
            f"The ``type`` slot carries the discriminator tag and the\n"
            f"``{payload_slot_name}`` slot carries the typed payload. Because the\n"
            f"payload class varies by tag, ``{payload_slot_name}`` is typed as\n"
            "``Any`` with an ``any_of`` union over the known payload classes;\n"
            "consumers select the concrete class using the table below.\n\n"
            f"{tag_mapping}"
        ),
        "attributes": {
            "type": {
                "description": f"Discriminator tag identifying the {name} variant.",
                "range": f"{name}Tag",
                "required": True,
            },
            payload_slot_name: {
                "description": "Typed payload; concrete class is selected by ``type``.",
                "range": "Any",
                "inlined": True,
                "required": True,
            },
        },
    }
    if payload_any_of:
        cls["attributes"][payload_slot_name]["any_of"] = payload_any_of
    return cls, tags


def _convert_jsonschema_module(
    js: dict,
    local: str,
    description: str,
    source_rel: str,
    see_also: list[str] | None = None,
) -> dict:
    """Convert a JSON-Schema 2020-12 document with ``$defs`` to a LinkML module.

    Handles only the subset needed for OPA's ``manifest.schema.json`` and
    ``plan.schema.json``: ``$defs`` are walked once, ``oneOf`` defs become
    tagged-wrapper classes, plain object defs become classes with attributes.
    """
    schema = schema_header(local, description)
    schema["imports"].append("./common")
    schema["source"] = _source_url(source_rel)
    if see_also:
        schema["see_also"] = see_also

    defs = js.get("$defs") or {}
    classes: dict[str, dict] = {}
    enums: dict[str, dict] = {}

    # Identify the root class (top-level ``$ref``).
    root_ref = js.get("$ref", "")
    root_name = root_ref.rsplit("/", 1)[-1] if root_ref else None

    # Identify aliases (defs that are themselves just ``$ref`` to another def)
    # so we can collapse them to ``is_a`` relationships rather than empty
    # standalone classes.
    aliases: dict[str, str] = {}
    for name, defn in defs.items():
        if set(defn.keys()) == {"$ref"} and defn["$ref"].startswith("#/$defs/"):
            aliases[name] = defn["$ref"].rsplit("/", 1)[-1]

    for name, defn in defs.items():
        if name in aliases:
            continue
        if "oneOf" in defn:
            cls, tags = _convert_oneof_def(name, defn)
            classes[name] = cls
            enums[f"{name}Tag"] = {
                "description": f"Permissible discriminator tags for the ``{name}`` union.",
                "permissible_values": {tag: {"description": tag} for tag in tags},
            }
        elif defn.get("type") == "object":
            classes[name] = _convert_object_def(name, defn)
        else:
            # Skip primitive/unknown defs; they are typically not modelled as
            # classes in LinkML.
            continue

    # Convert alias defs into ``is_a`` subclasses for clarity.
    for alias, target in aliases.items():
        if target in classes:
            classes[alias] = {
                "description": f"Alias for ``{target}`` (single-``$ref`` def in upstream JSON Schema).",
                "is_a": target,
            }

    if root_name and root_name in classes:
        classes[root_name]["tree_root"] = True

    schema["classes"] = classes
    if enums:
        schema["enums"] = enums
    return schema


