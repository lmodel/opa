"""Generate LinkML schema modules from Open Policy Agent source artifacts.

Reads upstream OPA source files and emits one LinkML schema submodule per
source under ``src/opa/schema/`` plus a corresponding instance YAML fixture
under ``tests/data/valid/``.

Sources:

* ``builtin_metadata.json``              -> ``builtin_metadata.yaml``
* ``capabilities.json``                  -> ``capabilities.yaml``
* ``v1/ast/version_index.json``          -> ``version_index.yaml``
* ``v1/schemas/authorizationPolicy.json`` -> ``authorization_input.yaml``
* ``v1/bundle/manifest.schema.json``     -> ``bundle_manifest.yaml``
* ``v1/ir/plan.schema.json``             -> ``ir_plan.yaml``

Run from the ``linkml/`` directory::

    uv run python scripts/gen_linkml.py all

or one module at a time::

    uv run python scripts/gen_linkml.py capabilities
"""

